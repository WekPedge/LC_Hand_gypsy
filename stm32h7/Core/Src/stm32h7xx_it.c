/**
  * @brief This function handles UART7 global interrupt.
  */
void UART7_IRQHandler(void)
{
		// 检查是否是“接收寄存器非空 (RXNE)”触发的中断
    if (__HAL_UART_GET_FLAG(&huart7, UART_FLAG_RXNE) != RESET)
    {
        // 1. 直接从硬件寄存器把数据掏出来 (这个动作会自动清除 RXNE 标志位)
        // 💡 注意：如果是 STM32F1/F4 系列，寄存器叫 DR
        //           如果是 STM32G4/H7 等较新系列，寄存器叫 RDR
        uint8_t byte_in = (uint8_t)(huart7.Instance->RDR & 0x00FF); 
        
        // 2. 喂给状态机
        Parse_UART_Data(byte_in);
				CAN_PackedPacket Pack1 = Pack_Servo_Positions(0x1FE, Target_Positions[0], Target_Positions[1], Target_Positions[2], Target_Positions[3], Target_Positions[4], Target_Positions[5]);
				CAN_PackedPacket Pack2 = Pack_Servo_Positions(0x1FF, Target_Positions[6], Target_Positions[7], Target_Positions[8], Target_Positions[9], Target_Positions[10], Target_Positions[11]);
				CAN_Expand_Transmit(&hfdcan1, Pack1.ExtID, Pack1.Data);
				CAN_Expand_Transmit(&hfdcan1, Pack2.ExtID, Pack2.Data);
    }
    else
    {
        // 如果是发送完成、或者其他错误中断（比如溢出 ORE），交给 HAL 库的默认处理去擦屁股
        HAL_UART_IRQHandler(&huart7);
    }
}

